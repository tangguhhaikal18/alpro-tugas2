# Tipe Data di Python dan Contohnya

# Tipe data Integer
integer_example = 42
# Integer adalah bilangan bulat, seperti 0, 1, -1, 100, dst.

# Tipe data Float
float_example = 3.14
# Float adalah bilangan desimal, seperti 3.14, 0.001, -2.5, dst.

# Tipe data String
string_example = "Hello, Python!"
# String adalah urutan karakter, seperti kalimat atau kata.

# Tipe data Boolean
boolean_example = True
# Boolean adalah tipe data yang hanya memiliki dua nilai: True atau False.

# Tipe data List
list_example = [1, 2, 3, "empat", 5.5]
# List adalah urutan dari beberapa elemen, bisa dari tipe data yang berbeda, dan bersifat mutable.

# Tipe data Tuple
tuple_example = (1, 2, 3, "empat", 5.5)
# Tuple mirip dengan list tetapi bersifat immutable, tidak bisa diubah setelah dibuat.

# Tipe data Dictionary
dict_example = {"nama": "Ali", "umur": 25}
# Dictionary adalah koleksi pasangan kunci-nilai, mirip dengan kamus.

# Tipe data Set
set_example = {1, 2, 3, 4, 5}
# Set adalah koleksi elemen unik yang tidak memiliki urutan.

# Menampilkan semua tipe data dan contohnya
print("Integer:", integer_example)
print("Float:", float_example)
print("String:", string_example)
print("Boolean:", boolean_example)
print("List:", list_example)
print("Tuple:", tuple_example)
print("Dictionary:", dict_example)
print("Set:", set_example)
