public class jawaban {
    public static void main(String[] args) {
        // Tipe data primitif di Java

        // byte (8-bit signed integer, nilai minimum -128 dan maksimum 127)
        byte tipeByte = 100;
        System.out.println("Tipe byte: " + tipeByte);

        // short (16-bit signed integer, nilai minimum -32,768 dan maksimum 32,767)
        short tipeShort = 30000;
        System.out.println("Tipe short: " + tipeShort);

        // int (32-bit signed integer, nilai minimum -2^31 dan maksimum 2^31-1)
        int tipeInt = 100000;
        System.out.println("Tipe int: " + tipeInt);

        // long (64-bit signed integer, nilai minimum -2^63 dan maksimum 2^63-1)
        long tipeLong = 10000000000L;
        System.out.println("Tipe long: " + tipeLong);

        // float (32-bit floating point, nilai decimal dengan ketelitian hingga 7 angka)
        float tipeFloat = 5.75f;
        System.out.println("Tipe float: " + tipeFloat);

        // double (64-bit floating point, nilai decimal dengan ketelitian hingga 15 angka)
        double tipeDouble = 19.99;
        System.out.println("Tipe double: " + tipeDouble);

        // char (16-bit Unicode character, berisi satu karakter)
        char tipeChar = 'A';
        System.out.println("Tipe char: " + tipeChar);

        // boolean (tipe data logika, berisi nilai true atau false)
        boolean tipeBoolean = true;
        System.out.println("Tipe boolean: " + tipeBoolean);

        // Tipe data non-primitif

        // String (sebuah array karakter atau rangkaian karakter)
        String tipeString = "Hello, Java!";
        System.out.println("Tipe String: " + tipeString);
    }
}
